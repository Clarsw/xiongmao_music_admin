Object.keys(filters).forEach(key => {
  Vue.filter(key, filters[key])
})

Vue.use(ElementUI)
Vue.use(VueRouter)
Vue.use(Vuex)

//NProgress.configure({ showSpinner: false });

const router = new VueRouter({
  routes
})

router.beforeEach((to, from, next) => {
  let user = JSON.parse(sessionStorage.getItem('user'));
  if (to.path == '/login') {
    if (!user) {
      next()
    } else {
      next({
        path: '/main'
      })
    }
  } else {
    if (user) {
      next()
    } else {
      next({
        path: '/login'
      })
    }
  }
})

